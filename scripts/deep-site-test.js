const publicExceptions = new Set(['funnel-map.html']);
const noindexOk = file => /<meta\s+name=["']robots["']\s+content=["'][^"']*noindex/i.test(read(file));

function stripQueryHash(url) { return url.split('#')[0].split('?')[0].trim(); }
function isExternal(raw) { return /^(https?:|mailto:|tel:|javascript:|data:)/i.test(raw) || raw.startsWith('#') || raw === ''; }
function hasAnchor(html, anchor) {
  if (!anchor) return true;
  const safe = anchor.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  return new RegExp(`\\sid=["']${safe}["']`, 'i').test(html) || new RegExp(`\\sname=["']${safe}["']`, 'i').test(html);
}

function localHtmlAudit() {
  const required = [
    'index.html','books.html','news.html','amazon-store-books.html','black-file.html','book-as-above-so-below.html',
    'start-here.html','search.html','matrix.js','styles.css','netlify.toml','sitemap.xml','robots.txt'
  ];
  for (const file of required) exists(file) ? pass(`Required file exists: ${file}`) : fail(`Missing required file: ${file}`);

  for (const file of htmlFiles) {
    const html = read(file);
    const internal = publicExceptions.has(file) || noindexOk(file);
    if (!internal) {
      for (const phrase of bannedPublicPhrases) {
        if (phrase.test(html)) warn(`${file}: public copy contains internal/build phrase: ${phrase}`);
      }
    }

    const attrRegex = /(?:href|src)=["']([^"']+)["']/gi;
    let match;
    while ((match = attrRegex.exec(html)) !== null) {
      const raw = match[1].trim();
      if (isExternal(raw)) continue;
      const [targetPart, anchor] = raw.split('#');
      const target = stripQueryHash(targetPart);
      if (!target) continue;
      const resolved = path.normalize(path.join(path.dirname(file), target)).replace(/\\/g, '/');
      if (resolved.startsWith('..')) { fail(`${file}: link escapes root: ${raw}`); continue; }
      if (!exists(resolved) && resolved !== 'downloads/the-black-file-matrix-reprogrammed.pdf') {
        fail(`${file}: missing link/asset target ${raw} -> ${resolved}`);
      } else if (anchor && resolved.endsWith('.html')) {
        if (!hasAnchor(read(resolved), anchor)) fail(`${file}: missing anchor target ${raw}`);
      }
    }
  }

  if (exists('news.html')) {
    const news = read('news.html');
    const cards = (news.match(/class=["'][^"']*news-item/g) || []).length;
    /Latest 7 Days/i.test(news) ? pass('news.html has Latest 7 Days') : fail('news.html missing Latest 7 Days');
    cards >= 3 ? pass(`news.html has ${cards} bulletin cards`) : fail(`news.html has only ${cards} bulletin cards`);
    /Human Cost Evidence Panel/i.test(news) ? pass('news.html has Human Cost Evidence Panel') : fail('news.html missing Human Cost Evidence Panel');
    /NCMEC/i.test(news) ? pass('news.html cites NCMEC data') : warn('news.html does not mention NCMEC');
    /desk is active|Candidate list updates|ready for automation/i.test(news) ? fail('news.html contains placeholder news wording') : pass('news.html has no known placeholder news wording');
  }

  if (exists('books.html')) {
    const books = read('books.html');
    /As Above, So Below/i.test(books) ? pass('books.html contains As Above, So Below') : fail('books.html missing As Above, So Below');
    /Masons in the United Nations/i.test(books) ? fail('books.html still contains unwritten Masons in the United Nations') : pass('books.html excludes Masons in the United Nations');
  }

  if (exists('search-index.json')) {
    const idx = read('search-index.json');
    /book-as-above-so-below\.html/i.test(idx) ? pass('search-index points to book-as-above-so-below.html') : fail('search-index missing book-as-above-so-below.html');
    /Masons in the United Nations/i.test(idx) ? fail('search-index contains unwritten Masons in the United Nations') : pass('search-index excludes Masons in the United Nations');
  }
}

async function fetchWithRetry(url, tries = 3) {
  let last;
  for (let i = 0; i < tries; i++) {
    try {
      const res = await fetch(url, { redirect: 'follow' });
      const text = await res.text();
      if (res.ok) return { ok: true, status: res.status, text, url: res.url };
      last = { ok: false, status: res.status, text, url: res.url };
    } catch (e) {
      last = { ok: false, status: 0, text: String(e.message || e), url };
    }
    await new Promise(r => setTimeout(r, 4000));
  }
  return last;
}

async function liveAudit() {
  const checks = [
    ['/', ['MATRIX REPROGRAMMED']],
    ['/books', ['THE BOOK ARCHIVE','As Above']],
    ['/books.html', ['THE BOOK ARCHIVE','As Above']],
    ['/news', ['Latest 7 Days','Human Cost Evidence Panel']],
    ['/news.html', ['Latest 7 Days','Human Cost Evidence Panel']],
    ['/amazon-store', ['THE BOOK WALL']],
    ['/amazon-store-books.html', ['THE BOOK WALL']],
    ['/black-file.html', ['Black File']],
    ['/book-as-above-so-below.html', ['As Above']]
  ];

  for (const [route, expected] of checks) {
    const url = liveBase.replace(/\/$/, '') + route;
    const res = await fetchWithRetry(url);
    if (!res.ok) { warn(`LIVE ${route}: HTTP/fetch problem status=${res.status}`); continue; }
    for (const needle of expected) {
      res.text.toLowerCase().includes(needle.toLowerCase()) ? pass(`LIVE ${route}: contains ${needle}`) : warn(`LIVE ${route}: missing ${needle}`);
    }
    if (/placeholder|mapping needed|exact ASIN|screenshots|user-provided/i.test(res.text)) warn(`LIVE ${route}: contains internal/build wording`);
  }
}

function writeReport() {
  const lines = [];
  lines.push('# Matrix Reprogrammed Deep Site Test Report');
  lines.push('');
  lines.push(`Date: ${new Date().toISOString()}`);
  lines.push(`Live base: ${liveBase}`);
  lines.push('');
  lines.push(`## Summary`);
  lines.push(`- Passes: ${passes.length}`);
  lines.push(`- Warnings: ${warnings.length}`);
  lines.push(`- Failures: ${failures.length}`);
  lines.push('');
  lines.push('## Failures');
  lines.push(failures.length ? failures.map(x => `- ${x}`).join('\n') : '- None');
  lines.push('');
  lines.push('## Warnings');
  lines.push(warnings.length ? warnings.map(x => `- ${x}`).join('\n') : '- None');
  lines.push('');
  lines.push('## Passes');
  lines.push(passes.map(x => `- ${x}`).join('\n'));
  fs.writeFileSync(reportPath, lines.join('\n'));
  console.log(lines.join('\n'));
}

(async () => {
  localHtmlAudit();
  await liveAudit();
  writeReport();
  if (strict && failures.length) process.exit(1);
})();
