const fs = require('fs');
const path = require('path');

const root = process.cwd();
const liveBase = process.env.LIVE_BASE || 'https://matrixreprogrammed.netlify.app';
const strict = process.env.DEEP_TEST_STRICT === '1';
const reportPath = path.join(root, 'deep-site-test-report.md');

const ignoredDirs = new Set(['.git', 'node_modules', '.netlify']);
const allFiles = new Set();
const htmlFiles = [];
const jsFiles = [];

function walk(dir) {
  if (!fs.existsSync(dir)) return;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (ignoredDirs.has(entry.name)) continue;
    const full = path.join(dir, entry.name);
    const rel = path.relative(root, full).replace(/\\/g, '/');
    if (entry.isDirectory()) walk(full);
    else {
      allFiles.add(rel);
      if (rel.endsWith('.html')) htmlFiles.push(rel);
      if (rel.endsWith('.js')) jsFiles.push(rel);
    }
  }
}

walk(root);

const failures = [];
const warnings = [];
const passes = [];

function pass(msg) { passes.push(msg); }
function warn(msg) { warnings.push(msg); }
function fail(msg) { failures.push(msg); }
function read(file) { return fs.readFileSync(path.join(root, file), 'utf8'); }
function exists(file) { return allFiles.has(file); }

