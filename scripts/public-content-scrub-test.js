const fs = require('fs');
const path = require('path');

const root = process.cwd();
const problems = [];
const warnings = [];

const publicExtensions = new Set(['.html', '.md']);
const skipDirs = new Set(['.git', 'node_modules', '_site']);
const criticalFiles = [
  'index.html',
  'control-system-tracker.html',
  'timers.html',
  'power-atlas.html',
  'atlas-index.html',
  'epstein-files.html',
  'live-intel.html',
  'search.html',
  'downloads/policy-convergence-tracker.md',
  'downloads/policy-convergence-timer-map.json',
  'downloads/global-risk-clocks-linked.json'
];

const forbiddenText = [
  /\bChatGPT\b/i,
  /\bOpenAI\b/i,
  /\bassistant\s*:/i,
  /\buser\s*:/i,
  /author[- ]facing/i,
  /author note/i,
  /developer note/i,
  /internal note/i,
  /TODO/i,
  /FIXME/i,
  /lorem ipsum/i,
  /placeholder copy/i,
  /insert .* here/i,
  /as an ai/i,
  /i cannot browse/i,
  /tool call/i,
  /python-docx/i,
  /generated by chat/i
];

const requiredVisibleMarkers = {
  'control-system-tracker.html': [
    'CONTROL SYSTEM TRACKER.',
    'Source Hierarchy',
    'Signal Levels',
    'Tracker Lanes',
    'Cross-System Links',
    'Risk Timers',
    'Boundary'
  ],
  'timers.html': [
    'GLOBAL RISK CLOCKS.',
    'RISK SIGNAL LANE',
    'Static page, not a live counter'
  ],
  'epstein-files.html': [
    'Evidence Boundary',
    'People / Entity Tracker',
    'Network Architecture Matrix'
  ],
  'power-atlas.html': [
    'Power Atlas',
    'Control System Tracker'
  ]
};

function walk(dir, out = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (skipDirs.has(entry.name)) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, out);
    else if (publicExtensions.has(path.extname(entry.name))) out.push(full);
  }
  return out;
}

function rel(file) { return path.relative(root, file).replace(/\\/g, '/'); }
function read(file) { return fs.readFileSync(file, 'utf8'); }
function textOnly(raw) {
  return raw
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/<!--[\s\S]*?-->/g, ' ')
    .replace(/<[^>]+>/g, ' ')
    .replace(/&nbsp;/g, ' ')
    .replace(/&gt;/g, '>')
    .replace(/&lt;/g, '<')
    .replace(/&amp;/g, '&')
    .replace(/\s+/g, ' ')
    .trim();
}

for (const file of criticalFiles) {
  const full = path.join(root, file);
  if (!fs.existsSync(full)) problems.push(`missing critical public output: ${file}`);
}

for (const full of walk(root)) {
  const relative = rel(full);
  const raw = read(full);
  const visible = textOnly(raw);
  for (const pattern of forbiddenText) {
    if (pattern.test(visible)) problems.push(`${relative}: public-facing text contains forbidden internal marker ${pattern}`);
  }
  if (/console\.log\(|process\.exit\(|require\(/.test(visible)) {
    warnings.push(`${relative}: visible text may expose script-like wording; inspect if intentional`);
  }
}

for (const [file, markers] of Object.entries(requiredVisibleMarkers)) {
  const full = path.join(root, file);
  if (!fs.existsSync(full)) continue;
  const visible = textOnly(read(full));
  for (const marker of markers) {
    if (!visible.includes(marker)) problems.push(`${file}: missing required visible marker ${marker}`);
  }
}

const report = {
  ok: problems.length === 0,
  checkedAt: new Date().toISOString(),
  problems,
  warnings
};
fs.writeFileSync(path.join(root, 'public-content-scrub-report.json'), JSON.stringify(report, null, 2));

if (warnings.length) {
  console.warn('PUBLIC CONTENT SCRUB WARNINGS');
  for (const warning of warnings) console.warn(`- ${warning}`);
}
if (problems.length) {
  console.error('\nPUBLIC CONTENT SCRUB TEST FAILED\n');
  for (const problem of problems) console.error(`- ${problem}`);
  console.error(`\n${problems.length} issue(s) found.\n`);
  process.exit(1);
}
console.log('PUBLIC CONTENT SCRUB TEST PASSED');
console.log('Checked public HTML/Markdown for chat markers, author-facing notes, placeholders, exposed internal wording, and required tracker/timer markers.');
